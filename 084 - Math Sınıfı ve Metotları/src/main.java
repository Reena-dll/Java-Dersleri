public class main {
    public static void main(String[] args) {
        System.out.println(Math.abs(-100)); // MUTLAK DEĞER
        System.out.println(Math.cbrt(8)); // KÜP KÖK
        System.out.println(Math.ceil(1.0001)); // Yuvalarma, üst sayıya yuvarlıyor.
        System.out.println(Math.floor(1.9999999)); // Yuvalarma, alt sayıya yuvarlıyor.
        System.out.println(Math.max(100,200));
        System.out.println(Math.pow(2,3));// ÜS ALMA
        System.out.println(Math.random()); // 0 ile 1 arasında bir sayı üretir.
        System.out.println(Math.random()*10); // 0 ile 10 arasında bir sayı üretir.
        System.out.println(Math.random()*100); // 0 ile 100 arasında bir sayı üretir.
        System.out.println(Math.round(10.1)); // En yakın tam sayıya yuvarlar !!!
        System.out.println(Math.signum(-100)); // Değer - ise -1 çevirir, değer + ise +1 çevirir.
        System.out.println(Math.sqrt(4)); // Karakök hesabı yapar
    }
}
